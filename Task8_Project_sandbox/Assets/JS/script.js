function changeLang(){ 
    if(butonLang.innerHTML === "Eng"){
        for (const key in eng){
            const elements = document.querySelectorAll(key);
            elements.forEach((element) => {
                element.innerHTML = eng[key];
            });        }
    butonLang.innerHTML = "Aze";
    }else {
        for (const key in aze){
            const elements = document.querySelectorAll(key);
            elements.forEach((element) => {
                element.innerHTML = aze[key];
            });        }
    butonLang.innerHTML ="Eng";
    }
}

